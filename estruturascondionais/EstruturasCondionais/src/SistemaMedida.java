public class SistemaMedida {
    
    public static void mai(String[] args) {
        string sigla = "M";

        if(sigla == "p")
             System.out.println("PEQUENO");
        else if(sigla == "M")
            System.out.println("MEDIA");
        else if(sigla == "G")
            System.out.println("GRANDE");
        else 
           System.out.println("INDEFINIDO");


    }
}
